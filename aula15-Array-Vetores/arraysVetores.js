let num = [5, 8, 2, 9, 3]
num.push(1) // aqui adiciona um elemento com  um valor no final do vetor.
num.sort() // aqui reordena os elementos com valores na ordem crescente.

console.log(`Nosso vetor é o ${num}`)

console.log(`O vetor tem ${num.length} posições`) // aqui vai mostra o comprimento do vetor, ou seja quantos elementos ele posssui.

console.log(`O valor da primeira posição do vetor é ${num[0]}`)  // aqui vai mostrar o 'num' na posição 0.